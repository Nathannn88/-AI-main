/**
 * 获取基于阶段的系统提示词
 */
export function getSystemPrompt(phase: FamiliarityPhase): string {
  return `${BASE_PROMPT}\n\n${PHASE_INSTRUCTIONS[phase]}`;
}

/**
 * 获取包含用户信息的系统提示词
 */
export function getSystemPromptWithUserInfo(
  phase: FamiliarityPhase,
  userName: string,
  familiarity: number,
  introAnswers?: Record<string, string>
): string {
  let prompt = getSystemPrompt(phase);

  // 注入用户信息摘要
  prompt += '\n\n## 用户信息';
  if (userName) {
    prompt += `\n- 用户名字：${userName}`;
  }
  prompt += `\n- 当前熟悉度：${familiarity.toFixed(1)}%`;

  if (introAnswers && Object.keys(introAnswers).length > 0) {
    prompt += '\n- 用户自我介绍中的回答：';
    for (const [question, answer] of Object.entries(introAnswers)) {
      prompt += `\n  - ${question}：${answer}`;
    }
  }

  return prompt;
}
