/**
 * 根据事件 ID 获取事件触发后的追加提示词（兼容旧版调用方式）
 * 支持旧版字符串 ID 和新版阈值数字
 */
export function getEventPrompt(eventId: string): string | null {
  // 兼容旧版事件 ID 格式（如 "event-20"、"event-a-first-resonance" 等）
  const thresholdMatch = eventId.match(/(\d+)/);
  if (thresholdMatch) {
    const threshold = parseInt(thresholdMatch[1], 10);
    const event = EVENT_PROMPTS[threshold];
    if (event) {
      return event.systemNote;
    }
  }

  return null;
}

/**
 * 获取事件的完整内容（包含台词和行为指令）
 */
export function getEventContent(threshold: number): EventPromptContent | null {
  return EVENT_PROMPTS[threshold] || null;
}
