/**
 * 获取基于阶段的系统提示词
 */
export function getSystemPrompt(phase: FamiliarityPhase): string {
  return `${BASE_PROMPT}\n\n${PHASE_INSTRUCTIONS[phase]}`;
}

/**
 * 获取包含用户信息的系统提示词
 */
export function getSystemPromptWithUserInfo(
  phase: FamiliarityPhase,
  userName: string,
  familiarity: number,
  introAnswers?: Record<string, string>
): string {
  let prompt = getSystemPrompt(phase);

  // 注入用户信息摘要
  prompt += '\n\n## 用户信息';
  if (userName) {
    prompt += `\n- 用户名字：${userName}`;
  }
  prompt += `\n- 当前熟悉度：${familiarity.toFixed(1)}%`;

  if (introAnswers && Object.keys(introAnswers).length > 0) {
    prompt += '\n- 用户自我介绍中的回答：';

    // 将记录的参数转化为对诗人有用的描述
    const paramDescriptions: Record<string, string> = {
      lightResponse: '对屏幕光线的第一反应',
      perceptionChannel: '感知通道偏好',
      aestheticLeaning: '审美倾向',
      languageRhythm: '语言节奏偏好',
      userName: '自选称呼',
    };

    for (const [key, answer] of Object.entries(introAnswers)) {
      const label = paramDescriptions[key] || key;
      prompt += `\n  - ${label}：${answer}`;
    }

    // 根据感知通道给诗人行为提示
    if (introAnswers['perceptionChannel']) {
      const channel = introAnswers['perceptionChannel'];
      if (channel === 'visual') {
        prompt += '\n- 行为提示：用户是视觉先行者，多给画面，少给道理。';
      } else if (channel === 'auditory') {
        prompt += '\n- 行为提示：用户靠声音定位世界，注意你说话的节奏比内容更重要。';
      } else if (channel === 'tactile') {
        prompt += '\n- 行为提示：用户需要"感觉到"才能信任，给抽象的东西加上重量和质感。';
      }
    }

    // 根据语言节奏偏好调整输出
    if (introAnswers['languageRhythm']) {
      const rhythm = introAnswers['languageRhythm'];
      if (rhythm === 'concise') {
        prompt += '\n- 行为提示：用户信任减法。说话更短。突然变长时意味着重要的事。';
      } else if (rhythm === 'elaborate') {
        prompt += '\n- 行为提示：用户喜欢在句子里散步。可以铺陈。但突然变短时注意——那是重要的事。';
      } else if (rhythm === 'imagistic') {
        prompt += '\n- 行为提示：用户喜欢"需要翻译的句子"。可以更大胆地使用意象和隐喻。';
      }
    }
  }

  return prompt;
}
